<h2><span>Chest Diseases</span></h2>
<p>Different types of diseases or conditions that can be experienced in the chest include pleurisy, flail chest, atelectasis, and the most common   condition, chest pain. These conditions can be caused by birth defects, trauma, or   in some cases because of heredity. It is important that all conditions are   discussed with a physician and not diagnosed to ensure that proper treatment is   given. Any condition that lowers the ability to breath deeply or decreases a   persons ability to cough is considered a   chest disease or condition.</p>
<h2>Chest Pain</h2>
<p>Chest pain can be the result of multiple issues including respiratory problems, digestive issues, musculoskeletal complications. The pain can   trigger cardiac issues as well. Not all pain that is felt is associated with the   heart, but it should not be taken lightly either. Symptoms can be different   depending on the cause of the pain. While cardiac   issues cause feelings of sudden pressure in the chest or a crushing pain in the   back, neck and arms, pain that is felt due to non cardiac issues gives a burning   feeling along the digestive track or pain when deep breaths are attempted. It is   important to understand that pains will be felt differently for each person even if they are experiencing the same condition. Only a patient will truly know if   the symptoms are something mild or serious.</p>
<p>Chest pain is most commonly associated with a heart attack. If this condition is present in the body, discomfort will be felt in the chest that is   similar to a heavy weight being placed on the body. Sweating, shortness of   breath, lightheadedness, and irregular   heartbeat can also be experienced. If heart attack occurs, the bulk of   damage is caused during the first six hours, so getting the proper treatment   quickly as possible is important. Some people, especially those who are elderly   or have diabetes, may not have typical chest pain but may have many of the other   symptoms of a heart attack. It is important that these patients and their care   givers have a good understanding of heart attack symptoms.</p>
