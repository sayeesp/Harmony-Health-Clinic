<h2>Diseases of the stomach</h2>
<p>Historically, it was widely believed that the highly acidic environment of the stomach would keep the stomach immune from infection.
However, a large number of studies have indicated that most cases of peptic ulcers, gastritis, and stomach cancer are caused by <em>Helicobacter pylori</em> infection.</p>
