      <p align="justify" class="style3">The food you eat is broken   down into a sugar called glucose. Glucose levels increase after a meal, but quickly   return to normal as body cells remove excess glucose from the bloodstream. A hormone   called insulin in the bloodstream helps cells take up glucose and use it as energy.   But in some people, either the pancreas cells do not make insulin or cells of   the body cannot use the insulin properly. Without insulin, cells are unable to   take up glucose. Instead, high levels of glucose builds up in the bloodstream   causing Diabetics (where it can cause damage to eyes, nerves, and blood vessels).</p>
      </p>
      <p align="justify" class="style3">There   are a number of factors, both genetic and environmental, that may increase a person's   risk of developing diabetes. People who have family members with diabetes are   at a higher risk for developing the disease. Although diabetes can run in families,   inheriting a particular gene mutation does not guarantee that a person will get   the disease. They must also have certain environmental factors that trigger diabetes,   which include obesity, inactivity, increasing age, and a history of gestational   diabetes.</p>
