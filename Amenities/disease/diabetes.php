<h4 class="style5">Symptoms
<hr /></h4>
<p align="justify" class="style3">The symptoms include </p>
<div align="justify" class="style3">
  <ul>
    <li>
      <p>Excessive urination</p>
    </li>
    <li>
      <p>Profound thirst and a dry mouth</p>
    </li>
    <li>
      <p>Increased hunger</p>
    </li>
    <li>
      <p>Fatigue, loss of weight</p>
    </li>
    <li>
      <p>Insufficient sleep because of the need to          urinate at night</p>
    </li>
    <li>
      <p>Itchiness of the vulva,            soreness and inflammation of the genital area in women</p>
    </li>
    <li>
      <p>Blurry vision </p>
    </li>
  </ul>
</div>
<p align="justify" class="style3">&nbsp;</p>
<p align="justify" class="style3">Although some of the symptoms of both forms of  diabetes are similar, type I diabetes tends to develop more quickly and become  more severe. </p>
