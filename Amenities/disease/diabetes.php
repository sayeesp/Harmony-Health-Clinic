<h4 align="justify"><span class="style5">Types</span>
<hr /></h4>
<p align="justify" class="style3">Diabetes are of two types. First    one appears in childhood, while the Second type normally affects people after    the age of 30 and is slightly more common in women than men.</p>
<p align="justify" class="style3"><strong><span class="style6">Type I diabetes</span>  </strong>This disorder usually  develops in childhood or adolescence and occurs when the pancreas  produces far too little insulin or produces none at all. </p>
<p align="justify" class="style3"><em><strong> Causes </strong></em> of type I  diabetes :  It  is usually caused by an abnormal bodily reaction, in which the immune system  destroys insulin-secreting cells in the pancreas. The child of a person who  has type I diabetes is at greater risk of developing the same type of diabetes. </p>
<p align="justify" class="style3"><span class="style6"><strong>Type II diabetes </strong></span> is the most common form of  diabetes normally affects people after  the age of 30 and is slightly more common in women than men.  This is a condition  in which pancreas continues to secrete insulin, but cells in the  body become  resistant to its effects. It develops slowly and is often unnoticed for many  years.  Here the body produces  insulin but not enough to cope with the levels of carbohydrate and sugar in the  diet.   </p>
<p align="justify" class="style3"><em><strong>Causes</strong></em> of type II diabetes : Genetics,  obesity, inactivity, age, history of gestational diabetics are the important factors  causing the type II diabetes.</p>
